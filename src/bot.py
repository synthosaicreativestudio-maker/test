def format_name(first_name: str, last_name: str, username: str) -> str:
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è"""
    parts = []
    if first_name:
        parts.append(first_name)
    if last_name:
        parts.append(last_name)
    
    if parts:
        result = ' '.join(parts)
        if username:
            result += f" (@{username})"
        return result
    elif username:
        return f"@{username}"
    else:
        return "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å"

